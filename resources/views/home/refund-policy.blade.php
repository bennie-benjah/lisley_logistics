                <h1 class="page-title">Refund & Return Policy</h1>
                <p class="page-subtitle">Our policies for returns, refunds, and exchanges of products and services.</p>

                <div class="legal-page">
                    <div class="legal-section">
                        <h2>Return Policy for Products</h2>
                        <p>Lilsley Logistics strives to ensure complete customer satisfaction with our logistics products. If you are not satisfied with your purchase, you may return eligible items within 30 days of delivery for a refund or exchange.</p>

                        <h3>Eligibility for Returns</h3>
                        <ul>
                            <li>Products must be returned in original condition with all packaging, manuals, and accessories</li>
                            <li>Items must not show signs of wear, damage, or misuse</li>
                            <li>Proof of purchase (order number or receipt) is required</li>
                            <li>Customized or special order products are not eligible for return unless defective</li>
                            <li>Software products may only be returned if unopened and unused</li>
                        </ul>

                        <h3>Return Process</h3>
                        <ol>
                            <li>Contact our customer service team to initiate a return request</li>
                            <li>Receive a Return Merchandise Authorization (RMA) number</li>
                            <li>Package the item securely with the RMA number clearly marked</li>
                            <li>Ship the item back using a trackable shipping method</li>
                            <li>Once received and inspected, we will process your refund or exchange</li>
                        </ol>

                        <h3>Refund Timeline</h3>
                        <p>Refunds are typically processed within 7-10 business days after we receive and inspect the returned item. The refund will be issued to the original payment method. Shipping charges are non-refundable unless the return is due to our error.</p>
                    </div>

                    <div class="legal-section">
                        <h2>Service Cancellation & Refunds</h2>
                        <p>For logistics services, cancellation policies vary based on the service type and timing of the cancellation request.</p>

                        <h3>Freight Services</h3>
                        <p>Cancellations made more than 24 hours before scheduled pickup are eligible for a full refund. Cancellations within 24 hours may incur a 50% cancellation fee. Once a shipment is in transit, cancellations are not possible, but rerouting may be available with applicable fees.</p>

                        <h3>Warehousing Services</h3>
                        <p>Warehouse storage agreements require 30 days written notice for termination. Prepaid storage fees for unused months will be refunded pro-rata. Setup fees are non-refundable once services have been initiated.</p>

                        <h3>Software Subscriptions</h3>
                        <p>Monthly software subscriptions can be cancelled at any time with no long-term commitment. Annual subscriptions cancelled within the first 30 days are eligible for a full refund. After 30 days, annual subscription refunds are calculated pro-rata for unused months.</p>
                    </div>

                    <div class="legal-section">
                        <h2>Damaged or Defective Items</h2>
                        <p>If you receive a damaged or defective product, please contact us within 48 hours of delivery. We will arrange for a replacement at no additional cost or provide a full refund. In some cases, we may require photographic evidence of the damage.</p>

                        <h3>Shipping Damage Claims</h3>
                        <p>For items damaged during shipping by our carriers, we will handle the claim process with the carrier on your behalf. You may choose to receive a replacement, repair, or refund while we process the claim with the carrier.</p>
                    </div>

                    <div class="legal-section">
                        <h2>Exchanges</h2>
                        <p>We gladly exchange products for a different size, color, or model within 30 days of purchase. The exchanged product must be in new condition with original packaging. Customer is responsible for shipping costs for exchanges unless the exchange is due to our error.</p>
                    </div>

                    <div class="legal-section">
                        <h2>Non-Refundable Items & Services</h2>
                        <ul>
                            <li>Customized or personalized products</li>
                            <li>Digital products that have been downloaded or accessed</li>
                            <li>Gift cards</li>
                            <li>Services already rendered or partially completed</li>
                            <li>Clearance or final sale items</li>
